number = int(input("Введіть число: "))

# Ініціалізуємо змінну для збереження суми
sum_of_numbers = 0

# Використовуємо цикл для додавання чисел від 1 до введеного числа
for i in range(1, number + 1):
    sum_of_numbers += i

# Виводимо суму на екран
print("Сума всіх чисел від 1 до", number, "включно:", sum_of_numbers)
