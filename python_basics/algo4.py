# Зчитування курсу євро до гривні та суми 
курс = float(input("Введіть курс євро до гривні: "))
сума_гривні = int(input("Введіть суму в гривнях, яку зібрали волонтери: "))

# Вартість 
вартість_бандеромобіля_євро = 6500

# Обчислення кількості бандеромобілів 
кількість_бандеромобілів = сума_гривні // (курс * вартість_бандеромобіля_євро)

# Виведення результату
print(кількість_бандеромобілів)



