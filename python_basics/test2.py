# Введення значень двох довжин суміжних сторін чотирикутника
a = float(input("Введіть довжину першої сторони: "))
b = float(input("Введіть довжину другої сторони: "))

# Перевірка, чи є фігура квадратом чи прямокутником
if a == b:
    print("Дана фігура - квадрат")
else:
    print("Дана фігура - прямокутник")
